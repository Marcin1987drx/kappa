export type Language = keyof typeof translations;

class I18n {
  private currentLanguage: Language = 'en';

  setLanguage(lang: Language): void {
    this.currentLanguage = lang;
    this.updateUI();
  }

  getLanguage(): Language {
    return this.currentLanguage;
  }

  getDateLocale(): string {
    const locales: Record<Language, string> = { en: 'en-US', de: 'de-DE', pl: 'pl-PL', ro: 'ro-RO' };
    return locales[this.currentLanguage] || 'en-US';
  }

  t(key: string): string {
    const keys = key.split('.');
    let value: any = translations[this.currentLanguage];
    
    for (const k of keys) {
      value = value[k];
      if (value === undefined) {
        return key;
      }
    }
    
    return value;
  }

  updateUI(): void {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach((element) => {
      const key = element.getAttribute('data-i18n');
      if (key) {
        element.textContent = this.t(key);
      }
    });

    // Update innerHTML translations (for keys containing HTML tags)
    const htmlElements = document.querySelectorAll('[data-i18n-html]');
    htmlElements.forEach((element) => {
      const key = element.getAttribute('data-i18n-html');
      if (key) {
        element.innerHTML = this.t(key);
      }
    });
    
    // Update placeholder translations
    const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
    placeholderElements.forEach((element) => {
      const key = element.getAttribute('data-i18n-placeholder');
      if (key) {
        (element as HTMLInputElement).placeholder = this.t(key);
      }
    });

    // Update title translations
    const titleElements = document.querySelectorAll('[data-i18n-title]');
    titleElements.forEach((element) => {
      const key = element.getAttribute('data-i18n-title');
      if (key) {
        (element as HTMLElement).title = this.t(key);
      }
    });
  }
}

export const i18n = new I18n();
